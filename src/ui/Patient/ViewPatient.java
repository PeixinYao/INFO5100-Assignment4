    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        Encounter e = (Encounter) jTable2.getValueAt(row, 0);
        ViewEncounter mpcjp = new ViewEncounter(userProcessContainer,e);
        userProcessContainer.add("ManageProductCatalogJPanel", mpcjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable2.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Encounter e = (Encounter) jTable2.getValueAt(row, 0);
        people.getEncounterHistory().removeEncounter(e);
        refreshTable();          // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

