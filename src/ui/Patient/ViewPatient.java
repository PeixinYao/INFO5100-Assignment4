    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
           if(jTextField2.getText().equals("")){
              JOptionPane.showMessageDialog(null, "encounter time can't be empty!!", "Warning", JOptionPane.WARNING_MESSAGE);
              return;
        }
           if(jTextField1.getText().equals("")){
              JOptionPane.showMessageDialog(null, "encounter hospital can't be empty!!", "Warning", JOptionPane.WARNING_MESSAGE);
              return;
        }
        if(people.getEncounterHistory()==null){
            EncounterHistory en=new EncounterHistory();
            people.setEncounterHistory(en);
        }
        Encounter e=people.getEncounterHistory().addEncounter();
        e.setEncounterTime(jTextField2.getText());
        e.setHospital(jTextField1.getText());
        refreshTable();
    }//GEN-LAST:event_jButton3ActionPerformed

