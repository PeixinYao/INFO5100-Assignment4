      public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        if(people.getEncounterHistory()!=null){
            for(Encounter e: people.getEncounterHistory().getEncounterHistory()){
            Object row[] = new Object[2];
            row[0]=e;
            row[1] =e.getHospital();
            model.addRow(row);
        }
        }
    }
